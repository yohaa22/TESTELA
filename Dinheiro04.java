public class Dinheiro04 {
    

    double precoDolar;
    double dolar;

    public Dinheiro04(double precoDolar , double dolar) {
     
        this.precoDolar = precoDolar;


        this.dolar = dolar;
       
    }



    public double reais() {
        return (precoDolar * dolar) ;
    }





 

}

